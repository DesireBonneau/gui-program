def envir(x, t_atom):  # x is the targeted atom of ligand
  """
  Identify the “environment” of a ligand atom by finding all heavy atoms
  in the target (macromolecule) that lie within a given distance range.

  Parameters
  ----------
  x : array-like of length 5
    The ligand atom record [ID, element, x, y, z].
  t_atom : ndarray of shape (M, 5)
    Array of macromolecule atoms; each row is [ID, element, x, y, z].

  Returns
  -------
  env : ndarray of shape (K, 2)
    Each row is [ID, element] of a macromolecule atom whose
    distance from x is between para[1] and para[0].
  """
  env = np.empty((0, 2)) # environment list containing atom ID and corresponding element from macromolecule
  steric = np.empty((0, 1)) # environment list containing only atom ID --> for counting disfavorable interactions
  # iterate over atoms in macromolecule
  for line in t_atom:
    if line[1] != 'H': # only care about heavy atoms
      # calculating euclidean distance between the ligand atom 'x' and a macromolecule atom "line"
      r = np.linalg.norm(np.array([float(x[2]), float(x[3]), float(x[4])]) - np.array([float(line[2]), float(line[3]), float(line[4])]))
      # if r is within range [para[0], para[1]] -> append to environment
      if r <= para[0] and r >= para[1]:
        env = np.append(env, np.array([[line[0],line[1]]]), axis=0)
        if r <= para[2]:
          steric = np.append(steric, np.array([[line[0]]]), axis=0)

  return env, steric


def hbond_score(l_atom, t_atom, l_SD, l_ND, l_WD, l_SA, l_NA, l_XX, t_SD, t_ND, t_WD, t_SA, t_NA, t_XX):
  """
  Compute counts of hydrogen bonds, charged interactions, salt bridges, and unsatisfied donors/acceptors
  between ligand and target atoms.

  Parameters
  ----------
  l_atom : ndarray of shape (L, 5)
      Ligand atom records.
  t_atom : ndarray of shape (T, 5)
      Target atom records.
  l_SD, l_ND, l_WD, l_SA, l_NA, l_XX : array-like
      IDs of ligand atoms classified as strong donors, normal donors, weak donors,
      strong acceptors, normal acceptors, and non-HBDA heavy atoms.
  t_SD, t_ND, t_WD, t_SA, t_NA, t_XX : array-like
      Same classifications for the target.

  Returns
  -------
  total_hbond_count : int
      Total number of hydrogen‐bonding interactions found.
  charged_hbond_count : int
      Count of interactions involving charged donors/acceptors.
  salt_bridge_count : int
      Count of salt bridges (strong donor + strong acceptor).
  unsatisfied_count : int
      Number of donors/acceptors with no partner in range.
  """
  total_hbond_count = 0
  charged_hbond_count = 0
  salt_bridge_count = 0
  unsatisfied_count = 0

  # iterate over each ligand atom
  for l in l_atom:
    print_list = []
    sub_total_hbond_count = 0
    sub_charged_hbond_count = 0
    sub_salt_bridge_count = 0
    sub_unsatisfied_count = 0
    # getting neighbours in range
    env, steric = envir(l, t_atom)

    # (*) if ligand atom's is normal donor and normal acceptor
    if l[0] in l_ND and l[0] in l_NA:  # both normal donor and acceptor
      print_list.append(l[1] + "_" +  l[0] + " (both normal donor and acceptor):")
      # iterate through the ligand atom's neighbours
      for env_atom in env:
        # if neighbour's in t_NA, t_ND, t_SA, t_SD, or t_WD -> increment relevant sub-list
        if env_atom[0] in t_NA or env_atom[0] in t_ND or env_atom[0] in t_SA or env_atom[0] in t_SD or env_atom[0] in t_WD:
          sub_total_hbond_count += 1
          # append to print list
          print_list.append("interaction with " + env_atom[1] + "_" + env_atom[0])
          if env_atom[0] in t_SA or env_atom[0] in t_SD:
            sub_charged_hbond_count += 1
      # if env != empty but there were no interactions -> ligand atom marked as unsatisfied
      if sub_total_hbond_count == 0 and len(env) != 0:  # unsatisfied H-bond
        for en in env:
          if en[0] in steric:
            sub_unsatisfied_count += 1
            print_list.append("penalized for disfavored interaction with " + str(env))

    # for normal donor only -> similar to (*)
    if l[0] in l_ND and l[0] not in l_NA:  # normal donor only
      print_list.append(l[1] + "_" +  l[0] + " (normal donor only):")
      for env_atom in env:
        if env_atom[0] in t_NA or env_atom[0] in t_SA:
          sub_total_hbond_count += 1
          print_list.append("interaction with " + env_atom[1] + "_" + env_atom[0])
          if env_atom[0] in t_SA:
            sub_charged_hbond_count += 1
      if sub_total_hbond_count == 0 and len(env) != 0:  # unsatisfied H-bond
        for en in env:
          if en[0] in steric:
            sub_unsatisfied_count += 1
            print_list.append("penalized for disfavored interaction with " + str(env))

    # for normal acceptor only -> similar to (*)
    if l[0] not in l_ND and l[0] in l_NA:  # normal acceptor only
      print_list.append(l[1] + "_" +  l[0] + " (normal acceptor only):")
      for env_atom in env:
        if env_atom[0] in t_ND or env_atom[0] in t_SD or env_atom[0] in t_WD:
          sub_total_hbond_count += 1
          print_list.append("interaction with " + env_atom[1] + "_" + env_atom[0])
          if env_atom[0] in t_SD:
            sub_charged_hbond_count += 1
      if sub_total_hbond_count == 0 and len(env) != 0:  # unsatisfied H-bond
        for en in env:
          if en[0] in steric:
            sub_unsatisfied_count += 1
            print_list.append("penalized for disfavored interaction with " + str(env))

    # for strong donor only -> similar to (*)
    if l[0] in l_SD:  # strong donor
      print_list.append(l[1] + "_" +  l[0] + " (strong donor):")
      for env_atom in env:
        if env_atom[0] in t_NA or env_atom[0] in t_SA:
          sub_total_hbond_count += 1
          print_list.append("interaction with " + env_atom[1] + "_" + env_atom[0])
          if env_atom[0] in t_NA:
            sub_charged_hbond_count += 1
          if env_atom[0] in t_SA:
            sub_salt_bridge_count += 1
      if sub_total_hbond_count == 0 and len(env) != 0:  # unsatisfied H-bond
        for en in env:
          if en[0] in steric:
            sub_unsatisfied_count += 1
            print_list.append("penalized for disfavored interaction with " + str(env))

    # for strong acceptor only -> similar to (*)
    if l[0] in l_SA:  # strong acceptor only
      print_list.append(l[1] + "_" +  l[0] + " (strong acceptor only):")
      for env_atom in env:
        if env_atom[0] in t_WD or env_atom[0] in t_ND or env_atom[0] in t_SD:
          sub_total_hbond_count += 1
          print_list.append("interaction with " + env_atom[1] + "_" + env_atom[0])
          if env_atom[0] in t_ND or env_atom[0] in t_WD:
            sub_charged_hbond_count += 1
          if env_atom[0] in t_SD:
            sub_salt_bridge_count += 1
      if sub_total_hbond_count == 0 and len(env) != 0:  # unsatisfied H-bond
        for en in env:
          if en[0] in steric:
            sub_unsatisfied_count += 1
            print_list.append("penalized for disfavored interaction with " + str(env))

    # for weak donor only -> similar to (*)
    if l[0] in l_WD:  # weak donor (no penalty for not interacting)
      print_list.append(l[1] + "_" +  l[0] + " (weak donor):")
      for env_atom in env:
        if env_atom[0] in t_NA or env_atom[0] in t_SA:
          sub_total_hbond_count += 1
          print_list.append("interaction with " + env_atom[1] + "_" + env_atom[0])
          if env_atom[0] in t_SA:
            sub_charged_hbond_count += 1

    # for non_HBDAs only -> similar to (*)
    if l[0] in l_XX: # non-HBDAs
      print_list.append(l[1] + "_" +  l[0] + " (non-HBDAs):")
      bad_interaction_count = 0
      for env_atom in env:
        if env_atom[0] in t_NA or env_atom[0] in t_ND or env_atom[0] in t_SA or env_atom[0] in t_SD: # no penalty for interacting weak donors
          bad_interaction_count += 1
      if bad_interaction_count != 0 and len(env) != 0:
        for en in env:
          if en[0] in steric:
            sub_unsatisfied_count += 1
            print_list.append("penalized for disfavored interaction with " + str(env))

    # if print_list > 1 then print
    if len(print_list) > 1:
      for result in print_list:
        log.write(result + "\n")

    # update general counts for each iteration of the ligand atoms
    total_hbond_count += sub_total_hbond_count
    charged_hbond_count += sub_charged_hbond_count
    salt_bridge_count += sub_salt_bridge_count
    unsatisfied_count += sub_unsatisfied_count

  return total_hbond_count, charged_hbond_count, salt_bridge_count, unsatisfied_count


