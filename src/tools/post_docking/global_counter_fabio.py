def extract(input,type):
  """
  Parse molecular coordinate and connectivity lines into structured arrays.

  Parameters
  ----------
  input_lines : list of str
    Lines of text representing atoms, bonds, and rings from a coordinate file.
  mode : str
    Parsing mode: 'target' to extract atoms and bonds for a target molecule;
    'ligand' to extract atoms, bonds, and ring definitions for a ligand.

  Returns
  -------
  atom : numpy.ndarray
    For 'target': array of shape (N, 5) with columns [atom_index, atom_type, x, y, z].
    For 'ligand': array of shape (N, 5) with columns [atom_id, atom_type, x, y, z].
  bond : numpy.ndarray
    Array of shape (M, 3) with columns [atom1_index, atom2_index, bond_order].
  ring : list of list of str or None
    Only in 'ligand' mode: a list of ring definitions (each a list of strings).
    None for 'target' mode.

  Notes
  -----
  - Uses regular expressions to identify atom, bond, and ring lines.
  - Coordinates are cast to floats; indices and bond orders to integers.
  """
  # parsing behaviour 1: target
  if type == "target":
    atom = np.empty((0, 5))
    bond = np.empty((0, 3))

    # iterates line by line
    for coor in input:
      # matching against the pattern below1 -> line represents an atom
      match1 = re.search(r"\s\d+\s[A-Za-z*]+\s[-]?\d+[.]?\d?\d?\d?\d?",coor)
      if match1:
        info = coor.split()
        # info[2]=atom index; info[3]=atom type; info[4],info[5],info[6]=X,Y,Z
        atom = np.append(atom, np.array([[int(info[2]), info[3], float(info[4]), float(info[5]), float(info[6])]]), axis=0)

      # matching against different pattern2 -> line represents bond line
      match2 = re.search(r"^\D\s+\S+\s\d+\s\d\s\d+\s\d+$",coor)
      if match2:
        info = coor.split()
        # info[4],info[5]=atom indices which bond connects; info[3]=bond order
        bond = np.append(bond, np.array([[int(info[4]), int(info[5]), int(info[3])]]), axis=0)
    return atom, bond

  # parsing behaviour 2: ligand
  if type == "ligand":
    atom = np.empty((0, 5))
    bond = np.empty((0, 3))
    ring = []
    # counter for atom numbering
    id = 0
    for coor in input:
      # matching against pattern1 below -> line represents an atom line
      match1 = re.search(r"\S+\S+\s+\S+\S+\s+\S+\S+\s\D",coor)
      if match1:
        id += 1
        info = coor.split()
        # info[3]=atom type; info[0],info[1],info[2]=X,Y,Z
        atom = np.append(atom, np.array([[int(id), info[3], float(info[0]), float(info[1]), float(info[2])]]), axis=0)

      # matching against pattern2 below -> line represents a bond line
      match2 = re.search(r"^\s*\d+\s+\d+\s+[1-9]\s+\d\s+",coor)
      if match2:
        info = coor.split()
        # info[0],info[1]=atom indices which bond connects; info[2]=bond order
        bond = np.append(bond, np.array([[int(info[0]), int(info[1]), int(info[2])]]), axis=0)

      # matching against pattern3 below -> line represents a ring definition line
      match3 = re.search(r" - \d+ - \d+ - \d+",coor)
      if match3:
        del info[-2:]
        info = coor.split(' - ')
        ring.append(info)
    return atom, bond, ring

