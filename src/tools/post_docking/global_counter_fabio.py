def planeinfo(A, B, C):
  """
  Compute the circumcenter and plane normal of triangle ABC.

  Parameters
  ----------
  A, B, C : array-like of length 3
      Coordinates of the three points defining the plane.

  Returns
  -------
  vector : ndarray shape (3,)
      Vector from the circumcenter to point A.
  normal : ndarray shape (3,)
      Normal vector of the plane ABC.
  circumcentre : ndarray shape (3,)
      Coordinates of the triangle’s circumcenter.
  """
  AC = A - C
  BC = B - C
  a = np.linalg.norm(AC)
  b = np.linalg.norm(BC)

  # getting a vector perpendicular to plane (cross product)
  cross = np.cross(AC, BC)
  c = np.linalg.norm(cross)
  circumcentre = (np.cross(((a*a)*BC - (b*b)*AC), cross))/(2*c*c) + C

  vector = A - circumcentre # vectors defined with circumcentre as origin
  normal = np.cross(A - circumcentre, B - circumcentre)
  return vector, normal, circumcentre


def pi_score(arom, l_vector, l_normal, l_circumcentre, t_aromatic, t_vectors, t_normals, t_circumcentres):
  """
  Evaluate π‐stacking geometry between one ligand ring and target rings.

  Parameters
  ----------
  arom : tuple
      Atom IDs of the ligand aromatic ring.
  l_vector : ndarray shape (3,)
      Vector from ligand ring circumcenter to one atom.
  l_normal : ndarray shape (3,)
      Plane normal of the ligand ring.
  l_circumcentre : ndarray shape (3,)
      Ligand ring circumcenter coordinates.
  t_aromatic : list of tuples
      Atom ID tuples for each target ring.
  t_vectors : list of ndarray shape (3,)
      Corresponding vectors for each target ring.
  t_normals : list of ndarray shape (3,)
      Plane normals for each target ring.
  t_circumcentres : list of ndarray shape (3,)
      Circumcenters of each target ring.

  Returns
  -------
  score_per_ring : float
      Calculated π‐stacking score (currently always 0.0).
  """
  print_list = []
  stack_count = 0
  l_centre = l_circumcentre
  P = l_vector  # plane vector P
  Q = l_normal  # plane normal Q
  p = np.linalg.norm(P)
  q = np.linalg.norm(Q)
  print_list.append(str(arom) + " (aromatic ring):")
  # iterating over each target ring
  for v in range(len(t_aromatic)):
    t_centre = t_circumcentres[v]
    distance = np.linalg.norm(l_centre - t_centre)
    if distance <= para[8]:
      N = t_normals[v]  # plane normal N
      n = np.linalg.norm(N)

      cos_alpha = np.clip(abs(np.dot(P, N)) / (p*n), 0.0, 1.0)
      alpha = math.acos(cos_alpha) * 180 / math.pi  # angle between plane vector from ligand and normal vector from target
      theta = abs(90.0 - alpha)  # pi stacking angle for parallel systems

      cos_beta = np.clip(abs(np.dot(Q, N)) / (q*n), 0.0, 1.0)
      beta = math.acos(cos_beta) * 180 / math.pi  # angle between plane normal from ligand and normal vector from target + pi stacking angle for edge-to-face systems

      # record a face-to-face interaction
      if theta <= para[9] and distance <= para[7]:
        print_list.append("pi-stacking (face-to-face/parallel-displaced) with " + str(t_aromatic[v]))
        stack_count += 1
      # record an edge-to-face interaction
      if beta >= para[10]:
        print_list.append("pi-stacking (edge-to-face) with " + str(t_aromatic[v]))
        stack_count += 1
  # only print the results if the score_per_ring ever changed
  if stack_count != 0:
    for result in print_list:
      log.write(result + "\n")
  if stack_count == 0:
    print_list.append("no pi-stacking interaction found")
  return stack_count

