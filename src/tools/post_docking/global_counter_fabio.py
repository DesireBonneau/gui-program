def bonding(atom_id, atom2, bond2):
  """
  Compute bonding information for a given atom.

  Parameters
  ----------
  atom_id : int
    The ID of the atom for which to gather bonding data.
  atom2 : ndarray of shape (N, 5)
    Array of atom records; each row is [ID, element, x, y, z].
  bond2 : ndarray of shape (M, 3)
      Array of bond records; each row is [atom1_ID, atom2_ID, bond_order].

  Returns
  -------
  total : int
    Sum of bond orders around the specified atom.
  bound_atom : ndarray of str
    Element symbols of atoms directly bonded to atom_id.
  bound_id : ndarray of int
    Numeric IDs of atoms directly bonded to atom_id.
  bound_atom_count : int
    Number of atoms bonded to atom_id.
  """
  # sum of bond orders around atom_id
  total = 0

  bound_atom = np.empty(0) # elements of bound atoms
  bound_id = np.empty(0) # ID of bound atoms
  # iterates through the bond entries
  for i in range(len(bond2)):
    # if bond involves target atom -> add bond order to total
    if int(bond2[i][0]) == atom_id or int(bond2[i][1]) == atom_id:
      total += int(bond2[i][2])
      # append the atom on the other end of the bond to bound_atom and bound_id lists
      if int(bond2[i][0]) == atom_id:
        bound_atom = np.append(bound_atom, atom2[int(bond2[i][1]) - 1][1])
        bound_id = np.append(bound_id, atom2[int(bond2[i][1]) - 1][0])
      else:
        bound_atom = np.append(bound_atom, atom2[int(bond2[i][0]) - 1][1])
        bound_id = np.append(bound_id, atom2[int(bond2[i][0]) - 1][0])
  bound_atom_count = len(bound_atom) # summing number of bound atoms
  return total, bound_atom, bound_id, bound_atom_count


def classify(atom,bond):
  """
  Classify each atom in a molecule according to hydrogen-bonding and conjugation roles.

  Parameters
  ----------
  atom : ndarray of shape (N, 5)
    Array of atom records; each row is [ID, element, x, y, z].
  bond : ndarray of shape (M, 3)
    Array of bond records; each row is [atom1_ID, atom2_ID, bond_order].

  Returns
  -------
  SD : ndarray of int
    Atom IDs classified as strong hydrogen-bond donors.
  ND : ndarray of int
    Atom IDs classified as normal hydrogen-bond donors.
  WD : ndarray of int
    Atom IDs classified as weak hydrogen-bond donors.
  SA : ndarray of int
    Atom IDs classified as strong hydrogen-bond acceptors.
  NA : ndarray of int
    Atom IDs classified as normal hydrogen-bond acceptors.
  sp2 : ndarray of int
    Atom IDs of sp²-hybridized centers.
  lone : ndarray of int
    Atom IDs that possess a lone pair (e.g. OH, NH₂).
  """
  SD = np.empty(0) # strong donors
  ND = np.empty(0) # normal donors
  WD = np.empty(0) # weak donors
  SA = np.empty(0) # strong acceptors
  NA = np.empty(0) # normal acceptors
  XX = np.empty(0) # heavy atoms which aren’t classified as donors/acceptors.
  sp2 = np.empty(0) # sp² centers
  lone = np.empty(0) # lone pair atoms
  conjR = np.empty(0) # conjugatable R=R
  conj_N = np.empty(0) # conjugated nitrogens

  # iterates through atom list
  for j in range(len(atom)):
    # C or P
    if atom[j][1] == 'C' or atom[j][1] == 'P': # carboxylates, phosphates
      # initialise new list "conj_O" to collect candidate oxygens
      conj_O = np.empty(0)
      ordersum, neighbor, neighbor_id, neighborsum = bonding(int(atom[j][0]), atom, bond)
      # iterate through current atom's neighbours
      for n in neighbor_id:
        # if neighbouring atom is oxygen
        if atom[int(n)-1][1] == 'O':
          nei_ordersum, nei_neighbor, nei_neighbor_id, nei_neighborsum = bonding(int(n), atom, bond)
          # if oxygen is bonded to exactly one atom -> append to "conj_0"
          if nei_neighborsum == 1:
            conj_O = np.append(conj_O, int(n))
      # if atom has >1 singly‐bound oxygen -> append atom to SA (strong acceptor) list
      if len(conj_O) > 1:
        for co in conj_O:
          SA = np.append(SA, str(int(co)))
          # carboxylic acid and phosphate conjugation ARE considered

    # handling unmarked O and S
    if (atom[j][1] == 'O' and atom[j][0] not in SA) or atom[j][1] == 'S':
      # similar computation as above for bonding info
      ordersum, neighbor, neighbor_id, neighborsum = bonding(int(atom[j][0]), atom, bond)
      if ordersum == 1:  # RO-, RS-
        # single bond only -> append atom to SA (strong acceptor) list
        SA = np.append(SA, atom[j][0])
      if ordersum == 2:
        if neighborsum == 1:  # R=O, R=S
          # double bond to one neighbour -> append atom to NA (normal acceptor) list
          NA = np.append(NA, atom[j][0])
          nei_ordersum, nei_neighbor, nei_neighbor_id, nei_neighborsum = bonding(int(atom[j][0]), atom, bond)
          for r in nei_neighbor_id:
            if r not in conjR:
              conjR = np.append(conjR, r) # conjugatable R=O, R=S
        if neighborsum == 2:  # ROR, RSR, ROH, RSH, H2O
          # two neighbours -> append to NA (normal acceptor) list && has a lone pair
          NA = np.append(NA, atom[j][0])
          lone = np.append(lone, atom[j][0])
          # if one neighbour is H, it can donate a H bond -> append to ND (normal donor) list
          if 'H' in neighbor:  # ROH, RSH, H2O
            ND = np.append(ND, atom[j][0])
            # sulfonium is not considered to be H-bond acceptor

    # F or Cl -> always NA (normal acceptor)
    if atom[j][1] == 'F' or atom[j][1] == 'Cl':  # R-F, R-Cl
      NA = np.append(NA, atom[j][0])

    # C
    if atom[j][1] == 'C':
      ordersum, neighbor, neighbor_id, neighborsum = bonding(int(atom[j][0]), atom, bond)
      # has at least one H and one electronegative neighbour -> weak donor
      if 'H' in neighbor:
        if 'O' in neighbor or 'N' in neighbor or 'F' in neighbor: # N/O/F-C-H
          WD = np.append(WD, atom[j][0])  # NCHB donors are considered the same regardless of the number/ charge of heteroatoms bound to the carbon
      # if bonded to 3 atoms -> sp²-hybridized so append C atom to conjR
      if neighborsum == 3:
        sp2 = np.append(sp2, atom[j][0])
        if atom[j][0] not in conjR:
          conjR = np.append(conjR, atom[j][0]) # other conjugatable C=X

    # N
    if atom[j][1] == 'N':
      ordersum, neighbor, neighbor_id, neighborsum = bonding(int(atom[j][0]), atom, bond)
      # if any of its neighbours are in conjR -> if so, append N atom to conj_N
      for n in neighbor_id:
        if n in conjR and atom[j][0] not in conj_N:
          conj_N = np.append(conj_N, atom[j][0]) # conjugated N:
      if ordersum == 3:  # R-CN, R=N-R, R=NH, R-NH2, R2NH, R3N
        # if three neighbors -> it has a lone pair
        if neighborsum == 3:  # R-NH2, R2NH, R3N
          lone = np.append(lone, atom[j][0])
          if atom[j][0] not in conj_N:
            NA = np.append(NA, atom[j][0]) # excluding cases with conjugated lone pair of N
        # if two neighbors -> normal acceptor & sp²
        if neighborsum == 2:  # R=N-R, R=NH
          NA = np.append(NA, atom[j][0])
          sp2 = np.append(sp2, atom[j][0])
        # if any H neighbor -> normal donor
        if 'H' in neighbor:  # R=NH, R-NH2, R2NH
          ND = np.append(ND, atom[j][0])
      # protenated N
      if ordersum == 4:
        # if H attached -> strong donor
        if 'H' in neighbor:  # R3NH+, R2NH2+, RNH3+, R2=NHR+
          SD = np.append(SD, atom[j][0])
        # if 3 neighbours ->  sp² center
        if neighborsum == 3:  # R=NR2+
          sp2 = np.append(sp2, atom[j][0])

    # certain metal ions -> strong donors
    if atom[j][1] in ['K', 'Na', 'Mg', 'Zn', 'Mn']:  # metals
      SD = np.append(SD, atom[j][0])

    # any unmarked heavy atom -> XX
    if (atom[j][0] not in SD and atom[j][0] not in ND and atom[j][0] not in WD and atom[j][0] not in SA and atom[j][0] not in NA) and atom[j][1] != 'H': # heavy atom of non-HBDAs
      XX = np.append(XX, atom[j][0])

  return SD, ND, WD, SA, NA, XX, sp2, lone

